/* 
 * fitsBits - return 1 if x can be represented as an 
 *  n-bit, two's complement integer.
 *   1 <= n <= 32
 *   Examples: fitsBits(5,3) = 0, fitsBits(-4,3) = 1
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 15
 *   Rating: 2
 */
int fitsBits(int x, int n) {
  /*If x can be represented as an n-bit integer, it's
  * 32 - n + 1 first bits must be the same, Just like 
  * type casting from short to long.
  */
  int mask = x >> (n + (~0));
  return !((x>>31)^mask);
}
/* 
 * getByte - Extract byte n from word x
 *   Bytes numbered from 0 (LSB) to 3 (MSB)
 *   Examples: getByte(0x12345678,1) = 0x56
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 6
 *   Rating: 2
 */
int getByte(int x, int n) {
  int mask=0xff;
  return (x>> (n<<3)) & mask;
}
/* 
 * isGreater - if x > y  then return 1, else return 0 
 *   Example: isGreater(4,5) = 0, isGreater(5,4) = 1
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 24
 *   Rating: 3
 */
int isGreater(int x, int y) {
  /* x > y equals to x >= y+1, so judge x - (y+1) is okey.
   * Mind overflow.
   */
  int judge = x + (~(1 + y) + 1); // judge = x-y
  int xm = (x>>31) + 1;// xm = 0 if x < 0;
  int ym = (y>>31) + 1;// ym = 0 if y < 0;
  judge = judge & (0x1<<31);
  
  return (xm & (~ym)) | ((xm | (~ym)) & (!(judge>>31)));
}
