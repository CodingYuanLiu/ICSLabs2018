int isNotEqual(int x, int y) {
  /* x-y != 0 and return 1*/
  int judge = x + (~y + 1);
  return !!judge;
}
/*
 * isPower2 - returns 1 if x is a power of 2, and 0 otherwise
 *   Examples: isPower2(5) = 0, isPower2(8) = 1, isPower2(0) = 0
 *   Note that no negative number is a power of 2.
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 60
 *   Rating: 4
 */
int isPower2(int x) {
  /* x is a power of 2 only when it has only one 1
   * and x>0 (Caution: x can't be 0).
  */
  int xm = 1 + (x>>31);//xm = 0 if x<0,1 otherwise.
  int mask = 0x11 | (0x11<<8);
  int cnt;
  mask = mask | (mask<<16);
  cnt = x & mask;
  cnt += x>>1 & mask;
  cnt += x>>2 & mask;
  cnt += x>>3 & mask;

  cnt = cnt + (cnt >> 16);

  mask = 0xf + (0xf<<8);
  cnt = (cnt & mask) + ((cnt>>4) & mask);
  cnt = cnt + (cnt>>8);
  cnt = cnt & 0x3f;
  return ((!(cnt>>1)) & xm & cnt);
  
}
/* 
 * leastBitPos - return a mask that marks the position of the
 *               least significant 1 bit. If x == 0, return 0
 *   Example: leastBitPos(96) = 0x20
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 6
 *   Rating: 4 
 */
int leastBitPos(int x) {
  /* x - 1 to set 0 to the leastBit.Then xor x to remove higher bits,
   * and x to remove lower bits.
  */
  return (x ^ (x + (~0))) & x;
}
/* 
 * logicalShift - shift x to the right by n, using a logical shift
 *   Can assume that 1 <= n <= 31
 *   Examples: logicalShift(0x87654321,4) = 0x08765432
 *   Legal ops: ~ & ^ | + << >>
 *   Max ops: 16
 *   Rating: 3 
 */
int logicalShift(int x, int n) {
  int mask = (~0)<<(33 + (~n));
  mask = ~mask;
  return (x>>n) & mask;
}
/*
 * satAdd - adds two numbers but when positive overflow occurs, returns
 *          maximum possible value, and when negative overflow occurs,
 *          it returns minimum positive value.
 *   Examples: satAdd(0x40000000,0x40000000) = 0x7fffffff
 *             satAdd(0x80000000,0xffffffff) = 0x80000000
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 30
 *   Rating: 4
 */
int satAdd(int x, int y) {
  int xm=(x>>31) + 1;//xm = 0 if xm<0.
  int ym=(y>>31) + 1;
  int sum=x+y;
  int xym=(sum>>31) + 1;
  /* When x and y have the same sign while x and
  * sum have different signs, overflow occurs.
  */
  int overflow = (!(xm ^ ym)) & (xm ^ xym);

  /* Get maximum positive or negative number:
   * If positive overflow occurs, sum < 0,return 
   * 0x7ffffff, so right shift 31 bits and non 
   * the most significant bit. 
  */

  int shift = (overflow<<5) + (~overflow + 1);
  return (sum>>shift) ^ (overflow<<31);
}
/* 
 * tc2sm - Convert from two's complement to sign-magnitude 
 *   where the MSB is the sign bit
 *   You can assume that x > TMin
 *   Example: tc2sm(-5) = 0x80000005.
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 15
 *   Rating: 4
 */
int tc2sm(int x) {
  /* If x > 0, don't change it. Else, use 31 bits to represent
   * -x, then set the most significant bit to 1.
  */
  int mask = x>>31;//mask = 0xffffffff if x < 0
  int xm = mask & 1;//xm = 1 if x < 0, xm = 0 otherwise.
  return ((xm + (x^mask)) | (xm<<31)) ;
}
